# Ref: http://www.sumsar.net/blog/2014/12/peter-norvigs-spell-checker-in-two-lines-of-r/
str_Peter_Norvig_spell_checker <- function(sentence, source){
  
  sorted_words <- names(sort(table(strsplit(tolower(source), "[^a-z]+")), decreasing = TRUE))
  words = unlist(strsplit(sentence, " "))
  
  correct <- function(word, sorted_words) { 
    c(sorted_words[ adist(word, sorted_words) <= min(adist(word, sorted_words), 2)], word)[1] 
  }
  for(i in 1:length(words)){
    corr = correct(words[i], sorted_words)
    if(corr != "" && corr != words[i])
      words[i] = corr
  }
  return(paste(words, collapse = " "))
}

