seg_words <- function(str1, str2){
  
  str2 = tolower(str2)
  str2 = gsub("[^a-z0-9./]"," ", str2)
  str2 = unique(unlist(strsplit(str2, " ")))[nchar(unique(unlist(strsplit(str2, " ")))) > 2]
  words = unlist(strsplit(tolower(str1), " "))
  s = c()
  for(word in words){
    if(nchar(word) > 3){
      s1 = c()
      s1 = append(s1, segmentit(word, str2, TRUE))
      if(length(s) > 1) s = append(s, s1[!s1 %in% c('er','ing','s','less') & nchar(s1) > 1])
      else s = append(s, word)
    }
    else s = append(s, word)
  }
  return(paste(s, collapse = " "))
}

segmentit <- function(s, txt_arr, t){
  st = s
  r = c()
  for(j in 1:nchar(s))
    for(word in txt_arr){
      s_without_last_j = paste(head(unlist(strsplit(s,"")), -j), collapse = "")
      if(word == s_without_last_j){
        #print(paste("word:",word))
        #print(paste("j:",j))
        r = append(r, s_without_last_j)
        #print(s[:-j],s[len(s)-j:])
        s = paste(unlist(strsplit(s,""))[nchar(s)-j:nchar(s)], collapse = "")
        r = append(r, segmentit(s, txt_arr, FALSE))
      }
    }
  if(t == TRUE) {
    i = nchar(paste(r, collapse = ""))
    if(i != nchar(st)) 
      append(r, paste(unlist(strsplit(st,""))[i:length(st)], collapse = ""))
  }
  return(r)
}

str_common_word <- function(str1, str2){
  words = unlist(strsplit(str1, " "))
  cnt = 0
  for(word in words)
    if(word != "") if(aregexec(word, str2, fixed = T)[[1]][1] >= 1) cnt = cnt + 1
    #if(regexpr(word, str2, fixed = T)[1] >= 1) cnt = cnt + 1
  
  return(cnt)
}

str_whole_word <- function(str1, str2, i_){
  cnt = 0
  while(i_ < nchar(str2)){
    #str_pos = regexpr(str1, substr(str2, i_, nchar(str2)), fixed = T)[1]
    str_pos = aregexec(str1, substr(str2, i_, nchar(str2)), fixed = T)[[1]][1]
    if(str_pos >= 1) i_ = i_ + str_pos - 1
    else i_ = str_pos

    if(i_ == -1)
      return(cnt)
    else
      cnt = cnt + 1
    i_ = i_ + nchar(str1)
  }
  return(cnt)
}

# Ref: http://www.r-statistics.com/2012/01/merging-two-data-frame-objects-while-preserving-the-rows-order/
# this function works just like merge, only that it adds the option to return 
# the merged data.frame ordered by x (1) or by y (2).
# This function has been updated by Mohammed to recover colmns' order.
merge.with.order <- function(x,y, ..., sort = T, keep_order){
  
  colnames <- unique(c(colnames(x), colnames(y)))
  add.id.column.to.data <- function(DATA)
  {
    data.frame(DATA, id... = seq_len(nrow(DATA)))
  }
  # add.id.column.to.data(data.frame(x = rnorm(5), x2 = rnorm(5)))
  order.by.id...and.remove.it <- function(DATA)
  {
    # gets in a data.frame with the "id..." column.  Orders by it and returns it
    if(!any(colnames(DATA)=="id...")) stop("The function order.by.id...and.remove.it only works with data.frame objects which includes the 'id...' order column")
    
    ss_r <- order(DATA$id...)
    ss_c <- colnames(DATA) != "id..."
    DATA[ss_r, ss_c]
  }
  
  order.columns <- function(DATA, colnames) DATA[,colnames]
  
  # tmp <- function(x) x==1; 1	# why we must check what to do if it is missing or not...
  # tmp()
  
  if(!missing(keep_order))
  {
    if(keep_order == 1) return(order.columns(order.by.id...and.remove.it(merge(x=add.id.column.to.data(x),y=y,..., sort = FALSE)), colnames))
    if(keep_order == 2) return(order.columns(order.by.id...and.remove.it(merge(x=x,y=add.id.column.to.data(y),..., sort = FALSE)), colnames))
    # if you didn't get "return" by now - issue a warning.
    warning("The function merge.with.order only accepts NULL/1/2 values for the keep_order variable")
  } else {return(merge(x=x,y=y,..., sort = sort))}
}

