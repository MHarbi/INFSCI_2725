sub_num <- function(w, strNum) {
  strNum <- as.data.frame(strNum)
  words <- unlist(strsplit(w, " "))
  nwords <- words
  for(i in 1:length(words)) {
    if(length(strNum[strNum$ltr == words[i], "num"]) > 0)
      
      tryCatch(nwords[i] <- as.character(strNum[strNum$ltr == words[i], "num"]),
               warning=function(w) {
                 cat("problem values: ", words[i], "result: ", as.character(strNum[strNum$ltr == words[i], "num"]), "\n")
               }
      )
      
  }
  return(nwords)
}

